function add(){
  return a += b
}

function subtract(){
  return a -= b
}

function multiply(){
  return a *= b
}

function divide(){
  return a /= b
}

function inc(n){
  return n += 1
}

function dec(n){
  return n -= 1
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}